const mongoose = require('mongoose')

const { PATIENT } = require('../constants/roles')


const Schema = mongoose.Schema;

// the Client is NOT the User
const PatientSchema = new Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    phone: {
        type: String,
        default: ''
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        default: PATIENT
    },
    bloodType: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});

module.exports = mongoose.model('Patients',PatientSchema);